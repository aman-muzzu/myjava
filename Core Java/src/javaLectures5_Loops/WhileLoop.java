package javaLectures5_Loops;

public class WhileLoop {

	/*
	 * 
	 * loops are not statements--->they are not terminated but exist.
	 * entry certeria--->loop condition must be true
	 * exist certeria--->loop condition is false
	 *
	 * 
	 */
	public static void main(String[] args) {
		
		int i=1;
		while(i<=10 ) {
			
			System.out.println(i);
			i++;
		}
		

	}

}
